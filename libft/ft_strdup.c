#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	size;

	size = ft_strlen(s1);
	str = malloc((size + 1) * sizeof(char));
	if (!str)
		return (0);
	ft_strlcpy(str, s1, size + 1);
	return (str);
}
